# In python, variables are used to store data that can be used  and manipulated later in the program. A  variable is created the moment you first assign a value to it using the assignment operator (=). 

# Python is a dynamically typed language, which means you don't need to declare the type of a variable when you create it. The interpreter determines the type based on the value assigned.
 
#Rule of declaring variables in Python:
# 1. Variable names must start with a letter (a-z, A-Z) or an underscore (_).
# 2. The rest of the variable name can contain letters, numbers (0-9), and underscores (_).
# 3. Variable names are case-sensitive (e.g., myVar and myvar are different variables).
# 4. Variable names cannot be the same as Python reserved keywords (like if, else, while, etc.). 

# Examples of variable declarations in Python:  

age = 34 #integer variable
cgpa = 7.67 #float variable
name = "Kaji Raju" #string variable
is_student = True #boolean variable

