# python support various data types to store different kinds of information. The most commonly used data types in Python include:
# 1. Integer (int): Represents whole numbers without decimal points.
# 2. Float (float): Represents real numbers with decimal points.
# 3. String (str): Represents a sequence of characters enclosed in quotes.
# 4. Boolean (bool): Represents either True or False values.
# 5. List (list): An ordered collection of items that can be modified.
# 6. Tuple (tuple): An ordered collection of items that cannot be modified.
# 7. Dictionary (dict): A collection of key-value pairs.
# 8. Set (set): An unordered collection of unique elements.


age = 25                          # Integer
height = 5.9                      # Float   
name = "Kaji Raju"                # String
is_student = True                 # Boolean
print(f"name: {name} age: {age} height: {height}, is_student: {is_student}")

