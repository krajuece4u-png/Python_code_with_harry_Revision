print("This is about escape sequences in Python")

# An example of escape sequences
print("He said, \"Hello! How are you?\"")  # Using \" to include double quotes
print('It\'s a beautiful day!')  # Using \' to include single quote
print("This is the first line.\nThis is the second line.")    # Using \n for new line
print("Column1\tColumn2\tColumn3")  # Using \t for tab spaces
print("This is a backslash: \\")  # Using \\ to print a backslash   
