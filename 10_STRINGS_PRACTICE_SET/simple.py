# PROBLEM 1: String creation and printing
# Create and print strings
print("=== PROBLEM 1 ===")
print("String creation")
print()

# Solution:
str1 = "Hello"
str2 = "World"
result = str1 + " " + str2
print(result)
print()

# PROBLEM 2: String length
# Find length of a string
print("=== PROBLEM 2 ===")
print("String length")
print()

# Solution:
text = "Python"
print(f"Length of '{text}': {len(text)}")
print()

# PROBLEM 3: String indexing
# Access characters by index
print("=== PROBLEM 3 ===")
print("String indexing")
print()

# Solution:
name = "Python"
print(f"First character: {name[0]}")
print(f"Last character: {name[-1]}")
print(f"Character at index 2: {name[2]}")
